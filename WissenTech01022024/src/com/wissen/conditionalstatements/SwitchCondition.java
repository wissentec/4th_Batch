package com.wissen.conditionalstatements;

public class SwitchCondition {

	public static void main(String[] args) {
		int day = 3;

		switch (day) {

		case 1:
			System.out.println("Day value equal to 1");
			break;
		case 2:
			System.out.println("Day value equal to 2");
			break;
		case 3:
			System.out.println("Day value equal to 3");
			break;
		default:
			System.out.println("Day value is " + day);

		}

	}

}
