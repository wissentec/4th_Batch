package com.wissen.loopingstatements;

public class NestedForLoop {
	
	public static void main(String[] args) {
		
		for(int i=0;i<=10;i++) {
			System.out.println("The value of i is ::"+i);
			for(int j=0;j<=5;j++) {
				System.out.println("The value of j is ::"+j);
			}
			
			
			
		}
		
		
	}
	

}
